#!/bin/bash
#=======================================================================
num_base=1
#-----------------------------------------------------------------------
tam_rede=${num_base}0000
#-----------------------------------------------------------------------
lamb0=0.0
#-----------------------------------------------------------------------
divisor=10
#-----------------------------------------------------------------------
alp=10.0
#-----------------------------------------------------------------------
n_lamb=500
#-----------------------------------------------------------------------
tRelax=500000
#-----------------------------------------------------------------------
tTotal=1500000
#-----------------------------------------------------------------------
interpola=0            # 0, continua; 1, dlb/2; 2, 1.0*dlb/4
                       # 3, 3 * dlb/4; 4, 1.0*dlb/3
                       # 5, 2 * dlb/3; -1, aceita o lamb0 dado aqui
#=======================================================================
#maquina='cluster'
maquina='casa'
#=======================================================================
#flags='-check all -traceback'
#-----------------------------------------------------------------------
#flags='-heap-arrays -O3 -g -fp-stack-check'
#-----------------------------------------------------------------------
#flags='-traceback -check all -heap-arrays -O3 -g -fp-stack-check'
#-----------------------------------------------------------------------
flags=''
#=======================================================================
dependencias='mod_rndgen_multiple.f90 geraRede.f90 mod_tools_redes.f90'
principal='main_SIRS_Estocastico_grafo_roda.f90'
#=======================================================================
nzeros=$( echo "scale=0; l(${tam_rede}/${num_base})/l(10) " | bc -l)
#=======================================================================
IFS='.'
#=======================================================================
alp_arr=( ${alp} )
#=======================================================================
IFS=' '
#=======================================================================
executavel='wheel_t'${num_base}${nzeros}'a'${alp_arr[0]}'p'${alp_arr[1]}
#=======================================================================
#================= Aqui deletamos o antigo executavel ==================
rm ${executavel} > erroRmExe.log
#=======================================================================
ifort ${dependencias} ${principal} ${flags} -o ${executavel}
#=======================================================================
rm -r *.mod
#=======================================================================
if [[ ${maquina} == 'casa' ]];then
   #--------------------------------------------------------------------
   time ./${executavel} ${tam_rede} ${lamb0} ${divisor} ${alp} ${n_lamb} ${tTotal} ${tRelax} ${interpola}
   #--------------------------------------------------------------------
elif [[ ${maquina} == 'cluster' ]];then
   #--------------------------------------------------------------------
   qsub -N ${executavel} -cwd executa_sirs_est_grafo_roda.sh ${executavel} ${tam_rede} ${lamb0} ${divisor} ${alp} ${n_lamb} ${tTotal} ${tRelax} ${interpola}
   #--------------------------------------------------------------------
else
   #--------------------------------------------------------------------
   echo "Maquina nao-reconhecida. Abortando script..."
   #--------------------------------------------------------------------
   exit
fi
#=======================================================================
